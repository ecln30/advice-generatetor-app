
const id = document.querySelector('.advice-id')
const advice = document.querySelector('.advice-text')

const dice = document.querySelector('.dice')

dice.addEventListener('click', showAdvice)

async function showAdvice() {
    
    console.log("Advice clicked")
    const response = await fetch("	https://api.adviceslip.com/advice")

    const data = await response.json()
    let text = data.slip.advice
    let Id = data.slip.id
    advice.textContent = `${text}`
    id.textContent = `Advice # ${Id}`
}















































































































































































































































































































